
CREATE TABLE EMP
(
  EMPNO     SMALLINT,
  ENAME     VARCHAR(10),
  JOB       VARCHAR(9),
  MGR       SMALLINT,
  HIREDATE  TIMESTAMP(0),
  SAL       DECIMAL(7,2),
  COMM      DECIMAL(7,2),
  DEPTNO    SMALLINT,
  ACCOUNT   TEXT
)
;

COMMENT ON COLUMN EMP.EMPNO IS '従業員番号';

COMMENT ON COLUMN EMP.ENAME IS '氏名';

COMMENT ON COLUMN EMP.JOB IS '職業';

COMMENT ON COLUMN EMP.MGR IS '直属の上司';

COMMENT ON COLUMN EMP.HIREDATE IS '入社日';

COMMENT ON COLUMN EMP.SAL IS '給与';

COMMENT ON COLUMN EMP.COMM IS '手当';

COMMENT ON COLUMN EMP.DEPTNO IS '部門番号';



CREATE TABLE DEPT
(
  DEPTNO  SMALLINT,
  DNAME   VARCHAR(14),
  LOC     VARCHAR(13)
)
;



CREATE TABLE EMP_FILE
(
  EMPNO      VARCHAR(4),
  FILE_NAME  VARCHAR(50),
  FILE_DATA  BYTEA
)
;



CREATE UNIQUE INDEX EMP_FILE_PK ON EMP_FILE
(EMPNO)
;



CREATE UNIQUE INDEX PK_DEPT ON DEPT
(DEPTNO)
;



CREATE UNIQUE INDEX PK_EMP ON EMP
(EMPNO)
;

ALTER TABLE EMP ADD PRIMARY KEY (EMPNO);

ALTER TABLE DEPT ADD PRIMARY KEY (DEPTNO);

ALTER TABLE EMP_FILE ADD PRIMARY KEY (EMPNO);


